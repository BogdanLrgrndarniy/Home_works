let number = prompt('Ведіть число від 0 до 9, щоб дізнатися спецсимвол');

if (number == 0) {
    alert(')');
} else if (number == 1) {
    alert('!');
} else if (number == 2) {
    alert('@');
} else if (number == 3) {
    alert('#');
} else if (number == 4) {
    alert('$');
} else if (number == 5) {
    alert('%');
} else if (number == 6) {
    alert('^');
} else if (number == 7) {
    alert('&');
} else if (number == 8) {
    alert('*');
} else if (number == 9) {
    alert('(');
} else {
    alert('Ведіть коректне значення');
}